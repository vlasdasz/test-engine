#[derive(Default)]
enum Case {
    #[default]
    Occlusion,
    Path,
}

#[view]
struct RenderTestView {
    case: Case,
}

impl ViewCallbacks for RenderTestView {
    fn before_render(&self, pass: &mut RenderPass) {
        match self.case {
            Occlusion => {
                render_occlusion(pass);
            }
            Path => {
                render_path(pass);
            }
        }
    }
}

